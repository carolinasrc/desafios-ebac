public class ClasseAnimal {

    /**
     * Classe Animal
     */

    String gato;
    String cachorro;
    String cavalo;
    String leão;


    /**
     *
     * @return variáveis do tipo String
     */

    public String getGato() {
        return gato;
    }

    public void setGato(String gato) {
        this.gato = gato;
    }

    public String getCachorro() {
        return cachorro;
    }

    public void setCachorro(String cachorro) {
        this.cachorro = cachorro;
    }

    public String getCavalo() {
        return cavalo;
    }

    public void setCavalo(String cavalo) {
        this.cavalo = cavalo;
    }

    public String getLeão() {
        return leão;
    }

    public void setLeão(String leão) {
        this.leão = leão;
    }
}